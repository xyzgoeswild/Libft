#include "libft.h"

int	ft_isalpha(int	c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
	{
		if (c >= 'A' && c <= 'Z')
		{
			return (1);
		}
		else if (c >= 'a' && c <= 'z')
		{
			return (2);
		}
	}
	return (0);
}
