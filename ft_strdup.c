#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*nstr;
	size_t	i;

	i = 0;
	nstr = (char *)malloc(sizeof(char) * ft_strlen(str) + 1);
	if (!nstr)
		return (NULL);
	while (str[i])
	{
		nstr[i] = str[i];
		i++;
	}
	nstr[i] = '\0';
	return (nstr);
}
